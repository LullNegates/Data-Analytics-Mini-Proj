def print_summary(result: dict) -> None:
    if not result:
        return
    games = result.get("games", [])
    if not games:
        print("  F3b -- No TAS comparisons available.")
        return

    print("\n  F3b -- Human WR vs TAS Best Time")
    hdr = f"  {'Game':<40} {'TAS(s)':<10} {'WR(s)':<10} {'Gap(s)':<9} {'%Closed':<14} {'Est.yrs':<9} Surpassed"
    print(hdr)
    print("  " + "-" * (len(hdr) - 2 + 12))

    for g in sorted(games, key=lambda x: x["game"]):
        yrs   = f"{g['est_years_to_match_tas']:.1f}" if g.get("est_years_to_match_tas") else "--"
        surp  = g.get("date_surpassed") or "--"
        mismatch_flag = " ⚠" if g.get("category_mismatch") else "  "
        proxy_flag    = " [proxy]" if g.get("tas_source_type") == "proxy" else ""

        pct_val = g.get("pct_closed")
        if pct_val is not None and pct_val > 100:
            pct_str = f"{pct_val:.1f}% (*surpassed)"
        elif pct_val is not None:
            pct_str = f"{pct_val:.1f}%"
        else:
            pct_str = "--"

        gap_s = g["current_gap_s"]
        print(
            f"  {g['game'] + mismatch_flag + proxy_flag:<42} "
            f"{g['tas_time_s']:<10.1f} "
            f"{g['current_wr_time_s']:<10.1f} "
            f"{gap_s:<9.3f} "
            f"{pct_str:<14} "
            f"{yrs:<9} "
            f"{surp}"
        )

    s = result.get("summary", {})
    if s:
        surp_count = s.get("human_surpassed_tas", 0)
        print(
            f"\n  Games: {s['n_games_with_tas']}  |  "
            f"WR surpassed TAS: {surp_count}  |  "
            f"Within 1s of TAS: {s.get('fully_matched_tas', 0)}  |  "
            f"Mean gap: {s.get('mean_gap_pct', 'n/a')}% of first WR"
        )
        print("  ⚠ = category mismatch (TAS branch differs from human Any% route)")


def print_tas_impact(result: dict) -> None:
    """Print the per-TAS-event impact table (Bannister Effect evidence)."""
    impact = result.get("tas_impact", {})
    events = impact.get("events", [])
    if not events:
        print("  F3b+ -- No TAS impact events computed.")
        return

    w = impact.get("window_days", IMPACT_WINDOW_DAYS)
    print(f"\n  F3b+ -- TAS Release Impact on WR (window = {w} days after each TAS)")
    print(f"  {'Game':<32} {'TAS Date':<12} {'TAS(s)':<9} {'WR@TAS':<9} "
          f"{'WR+{0}d'.format(w):<9} {'Δ(s)':<8} {'WRs':<5} {'Vel×'}")
    print("  " + "-" * 94)

    current_game = ""
    for ev in events:
        game_label = ev["game"] if ev["game"] != current_game else ""
        current_game = ev["game"]

        wr_post = f"{ev['wr_post_s']:.1f}" if ev["wr_post_s"] is not None else "--"
        wr_at   = f"{ev['wr_at_tas_s']:.1f}" if ev["wr_at_tas_s"] is not None else "--"
        delta   = f"{-ev['improvement_s']:+.1f}" if ev["improvement_s"] != 0 else "  0.0"
        ratio   = f"{ev['velocity_ratio']:.2f}×" if ev["velocity_ratio"] is not None else "  --"

        print(
            f"  {game_label:<32} {ev['tas_date']:<12} {ev['tas_time_s']:<9.1f} "
            f"{wr_at:<9} {wr_post:<9} {delta:<8} {ev['n_wrs_in_window']:<5} {ratio}"
        )

    imp = impact.get("summary", {})
    if imp:
        mr = imp.get("mean_velocity_ratio")
        mr_str = f"{mr:.2f}×" if mr is not None else "n/a"
        gt1  = imp.get("n_with_velocity_ratio_gt1", 0)
        total = imp.get("n_tas_events", 0)
        # n_with_any_new_wr is a better denominator for velocity ratio (only those cases have a ratio)
        n_ratio = sum(1 for ev in events if ev.get("velocity_ratio") is not None)
        faster_str = f"{gt1}/{n_ratio}" if n_ratio else "n/a"
        print(
            f"\n  {imp['n_with_wr_improvement']}/{total} TAS releases → WR improved  |  "
            f"{imp['n_with_any_new_wr']}/{total} → ≥1 new WR  |  "
            f"Mean Δ: {imp['mean_improvement_s']:.1f}s  |  "
            f"Mean vel×: {mr_str}  |  "
            f"Faster-after: {faster_str}"
        )
