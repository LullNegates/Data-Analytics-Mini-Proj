def _wr_velocity_in_window(wr_rows: list[dict],
                            start_date: str, end_date: str) -> float | None:
    """
    Average WR improvement rate (s/year) for WRs with date in [start_date, end_date].

    Returns None if fewer than 2 WRs exist in the window (can't compute a rate).
    """
    in_window = [r for r in wr_rows if start_date <= r["date"] <= end_date]
    if len(in_window) < 2:
        return None
    span_days = max(
        1,
        (_date.fromisoformat(in_window[-1]["date"])
         - _date.fromisoformat(in_window[0]["date"])).days,
    )
    total_improvement = float(in_window[0]["time_seconds"]) - float(in_window[-1]["time_seconds"])
    return round(total_improvement / span_days * 365, 4)


def _compute_tas_impact(
    game: str,
    wr_rows: list[dict],
    tas_timeline: list[dict],
    window_days: int = 180,
) -> list[TasImpactWindow]:
    """
    For each TAS record in `tas_timeline`, measure the human WR improvement
    in the `window_days` period after the TAS release.

    tas_timeline entries must have keys: "date" (ISO str), "time_s" (float).
    wr_rows must be sorted chronologically and have "date", "time_seconds".

    Skips TAS entries that predate the human WR data entirely (no wr_at_tas).
    """
    if not wr_rows or not tas_timeline:
        return []

    results: list[TasImpactWindow] = []

    for tas_entry in tas_timeline:
        tas_date = tas_entry["date"]
        tas_time = tas_entry["time_s"]

        # WR active on the TAS date = last WR with date <= tas_date
        before = [r for r in wr_rows if r["date"] <= tas_date]
        if not before:
            continue  # TAS predates human WR data — skip
        wr_at_tas = float(before[-1]["time_seconds"])

        # WRs set strictly after the TAS, within the window
        from datetime import date as _d, timedelta
        tas_d  = _d.fromisoformat(tas_date)
        end_d  = tas_d + timedelta(days=window_days)
        end_str = end_d.isoformat()
        start_str = (_d.fromisoformat(tas_date) - timedelta(days=window_days)).isoformat()

        in_window = [r for r in wr_rows if tas_date < r["date"] <= end_str]
        n_wrs = len(in_window)

        if in_window:
            wr_post = float(min((r["time_seconds"] for r in in_window), key=float))
        else:
            wr_post = None

        improvement_s   = round(wr_at_tas - wr_post, 3) if wr_post is not None else 0.0
        improvement_pct = round(improvement_s / wr_at_tas * 100, 3) if wr_at_tas > 0 else 0.0

        vel_pre  = _wr_velocity_in_window(wr_rows, start_str, tas_date)
        vel_post = _wr_velocity_in_window(wr_rows, tas_date, end_str)

        if vel_pre and vel_pre > 0 and vel_post is not None:
            vel_ratio = round(vel_post / vel_pre, 3)
        else:
            vel_ratio = None

        results.append(TasImpactWindow(
            game                = game,
            tas_date            = tas_date,
            tas_time_s          = round(tas_time, 3),
            wr_at_tas_s         = round(wr_at_tas, 3),
            wr_post_s           = round(wr_post, 3) if wr_post is not None else None,
            improvement_s       = improvement_s,
            improvement_pct     = improvement_pct,
            n_wrs_in_window     = n_wrs,
            vel_pre_s_per_year  = vel_pre,
            vel_post_s_per_year = vel_post,
            velocity_ratio      = vel_ratio,
            window_days         = window_days,
        ))

    return results


def _impact_summary(windows: list[TasImpactWindow]) -> dict:
    """Aggregate statistics across all per-TAS impact windows."""
    if not windows:
        return {}

    with_improvement = [w for w in windows if w.improvement_s > 0]
    with_any_wr      = [w for w in windows if w.n_wrs_in_window > 0]
    ratios           = [w.velocity_ratio for w in windows if w.velocity_ratio is not None]

    return {
        "n_tas_events":              len(windows),
        "n_with_wr_improvement":     len(with_improvement),
        "pct_with_wr_improvement":   round(len(with_improvement) / len(windows) * 100, 1),
        "n_with_any_new_wr":         len(with_any_wr),
        "pct_with_any_new_wr":       round(len(with_any_wr) / len(windows) * 100, 1),
        "mean_improvement_s":        round(sum(w.improvement_s for w in windows) / len(windows), 3),
        "mean_n_wrs_in_window":      round(sum(w.n_wrs_in_window for w in windows) / len(windows), 2),
        "mean_velocity_ratio":       round(sum(ratios) / len(ratios), 3) if ratios else None,
        "n_with_velocity_ratio_gt1": sum(1 for r in ratios if r > 1),
    }


