"""
F3b TAS Analysis -- Human WR vs Tool-Assisted Speedrun Comparison

TAS reference times are loaded from per-game tas_*.json files in
data/reference/ (generated by Dataset/fetch_tas.py and
Dataset/curate_tas_external.py). The old tas_known.json is superseded
and kept only as a legacy backup.

For each game WITH a known TAS reference this module computes:
  - Current gap (human WR − TAS time) in seconds and as % of first WR
  - Historical gap timeline: how the gap narrowed across the WR progression
  - Gap closing velocity (s/year) from the last 5 WRs
  - Forward projection: estimated years until human WR matches TAS
  - Date surpassed: the first WR date that equalled or beat the TAS time

Academic framing (Wooten 2022 -- "Bannister Effect"):
TAS is an "existence proof" for a route -- once TAS demonstrates a strategy
is possible, the human RTA community works to adopt it. This module
quantifies that diffusion process game by game.

Reads:  data/clean/wr_progression.csv
        data/reference/tas_*.json          (per-game TAS timelines)
        data/analysis/q3_stats.json        (for model floor, if available)
Writes: data/analysis/f3b_tas_stats.json
"""

