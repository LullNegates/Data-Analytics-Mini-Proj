IMPACT_WINDOW_DAYS = 180  # days after each TAS release to measure WR impact


def run() -> dict:
    wr_prog      = _load_csv("wr_progression.csv")
    tas_by_norm  = _load_tas_from_files()      # keyed by normalized name
    model_floors = _load_model_floors()

    # Group WR rows by game
    by_game: dict[str, list] = {}
    for r in wr_prog:
        by_game.setdefault(r["game"], []).append(r)

    results: list[TasComparisonResult] = []
    all_impact: list[TasImpactWindow]  = []
    skipped: list[dict] = []

    for game, game_rows in sorted(by_game.items()):
        norm = _normalize_name(game)
        tas_info = tas_by_norm.get(norm)

        if tas_info is None:
            skipped.append({"game": game, "reason": "no TAS timeline file found"})
            continue

        result = _analyse_game(game, game_rows, tas_info, model_floors)
        if result:
            results.append(result)
        else:
            skipped.append({"game": game, "reason": "insufficient WR data or TAS >= first WR"})

        # Per-TAS impact windows (requires full timeline, not just current best)
        tas_timeline = tas_info.get("timeline", [])
        if tas_timeline:
            windows = _compute_tas_impact(
                game, game_rows, tas_timeline, window_days=IMPACT_WINDOW_DAYS
            )
            all_impact.extend(windows)

    summary       = _cross_game_summary(results)
    impact_summary = _impact_summary(all_impact)

    output = {
        "analysis":    "f3b_tas_comparison",
        "description": (
            "Human WR vs TAS comparison. TAS times sourced from per-game "
            "tas_*.json files (fetch_tas.py + curate_tas_external.py). "
            "Games without a comparable TAS are listed in 'skipped'."
        ),
        "academic_context": (
            "Wooten (2022, Production and Operations Management) formally models "
            "the Bannister Effect: benchmark innovations stimulate subsequent progress "
            "through route/technique diffusion. TAS serves as that benchmark in speedrunning -- "
            "once TAS demonstrates a strategy, the human community works to adopt it."
        ),
        "games":          [r.to_dict() for r in results],
        "skipped":        skipped,
        "summary":        summary,
        "tas_impact": {
            "window_days":  IMPACT_WINDOW_DAYS,
            "description":  (
                f"For each TAS record release, measures the human WR improvement "
                f"in the {IMPACT_WINDOW_DAYS} days that follow. "
                "velocity_ratio > 1 means the community improved faster after "
                "the TAS than before (Bannister Effect)."
            ),
            "events":   [w.to_dict() for w in all_impact],
            "summary":  impact_summary,
        },
    }

    ANALYSIS_DIR.mkdir(parents=True, exist_ok=True)
    out = ANALYSIS_DIR / "f3b_tas_stats.json"
    out.write_text(
        json.dumps(normalize_floats(output), ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    print(f"  [f3b] written -> {out.name}  ({len(results)} games with TAS, {len(skipped)} skipped, "
          f"{len(all_impact)} TAS-impact events)")
    return output


