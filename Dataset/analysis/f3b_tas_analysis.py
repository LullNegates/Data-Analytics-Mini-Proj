def _normalize_name(name: str) -> str:
    """Strip accents and lowercase for fuzzy game-name matching."""
    nfkd = unicodedata.normalize("NFKD", name)
    ascii_name = "".join(c for c in nfkd if not unicodedata.combining(c))
    return ascii_name.lower().strip()


def _load_tas_from_files() -> dict[str, dict]:
    """
    Load TAS reference data from per-game tas_*.json files.

    Returns a dict keyed by *normalized* game name (accents stripped,
    lower-cased) mapping to a flat dict compatible with _analyse_game():

        {
          "tas_time_s":       float,
          "tas_date":         str | None,
          "source":           str,
          "source_url":       str,
          "status":           str,
          "category_mismatch": bool,
          "tas_source_type":  str,   # "tasvideos" | "external" | "proxy"
        }
    """
    result: dict[str, dict] = {}
    for f in sorted(REFERENCE_DIR.glob("tas_*.json")):
        if f.name == "tas_known.json":
            continue
        try:
            data = json.loads(f.read_text(encoding="utf-8"))
        except Exception:
            continue

        game = data.get("game", "")
        if not game:
            continue

        status = data.get("status", "")
        if status == "no_tas_available":
            continue

        current_best = data.get("current_best")
        if not current_best or current_best.get("time_s") is None:
            continue

        game_id = data.get("tasvideos_game_id")
        if status == "found_proxy":
            src_type = "proxy"
        elif game_id:
            src_type = "tasvideos"
        else:
            src_type = "external"

        result[_normalize_name(game)] = {
            "game":              game,          # canonical display name from file
            "tas_time_s":        current_best["time_s"],
            "tas_date":          current_best.get("date"),
            "source":            data.get("source", ""),
            "source_url":        data.get("source_url", ""),
            "status":            status,
            "category_mismatch": data.get("category_mismatch", False),
            "tas_source_type":   src_type,
            # Full improvement timeline for TAS-impact analysis
            "timeline": [
                {"date": e["date"], "time_s": e["time_s"]}
                for e in data.get("timeline", [])
                if e.get("date") and e.get("time_s") is not None
            ],
        }

    return result


def _load_tas_reference() -> dict:
    """Legacy loader for tas_known.json. Kept for backward-compat with older tests."""
    path = REFERENCE_DIR / "tas_known.json"
    if not path.exists():
        raise FileNotFoundError("tas_known.json not found in data/reference/")
    with open(path, encoding="utf-8") as f:
        return json.load(f)


def _load_csv(name: str) -> list[dict]:
    path = CLEAN_DIR / name
    if not path.exists():
        raise FileNotFoundError(f"{name} not found -- run clean.py first")
    with open(path, encoding="utf-8") as f:
        return list(csv.DictReader(f))


def _load_model_floors() -> dict[str, float]:
    path = ANALYSIS_DIR / "q3_stats.json"
    if not path.exists():
        return {}
    with open(path, encoding="utf-8") as f:
        q3 = json.load(f)
    floors: dict[str, float] = {}
    for entry in q3.get("tas_proximity", []):
        if entry.get("floor_model") not in (None, "none_detected"):
            floors[entry["game"]] = float(entry["theoretical_floor_s"])
    return floors


